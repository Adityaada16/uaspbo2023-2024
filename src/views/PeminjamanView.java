    private void loadBarangToComboBox() {
        InventarisController inventarisController = new InventarisController();
        List<Inventaris> listBarang = inventarisController.getAllBarang();

        cbBarang.removeAllItems(); // Kosongkan terlebih dahulu
        barangMap.clear(); // Bersihkan `HashMap` barang

        for (Inventaris barang : listBarang) {
            String namaBarang = barang.getNamaBarang();
            int idBarang = barang.getIdBarang();

            cbBarang.addItem(namaBarang); // Tambahkan nama barang ke ComboBox
            barangMap.put(namaBarang, idBarang); // Simpan relasi nama dan ID di HashMap
        }
    }

    private void loadPeminjamToComboBox() {
        AnggotaController anggotaController = new AnggotaController();
        List<Anggota> listAnggota = anggotaController.getAllAnggota();

        cbAnggota.removeAllItems(); // Kosongkan terlebih dahulu
        peminjamMap.clear(); // Bersihkan `HashMap` peminjam

        for (Anggota anggota : listAnggota) {
            String namaAnggota = anggota.getNama();
            int idAnggota = anggota.getIdAnggota();

            cbAnggota.addItem(namaAnggota); // Tambahkan nama anggota ke ComboBox
            peminjamMap.put(namaAnggota, idAnggota); // Simpan relasi nama dan ID di HashMap
        }
    }

    private void setTanggalHariIni() {
        // Dapatkan tanggal hari ini
        LocalDate today = LocalDate.now();

        // Tentukan format tanggal
        DateTimeFormatter formatter = DateTimeFormatter.ofPattern("yyyy-MM-dd");

        // Format tanggal sebagai string
        String formattedDate = today.format(formatter);

        // Setel tanggal ke field TfTanggal
        TfTglPeminjaman.setText(formattedDate);
        TfTglPengembalian.setText(formattedDate);
    }

    private void showDataTable() {
        // Membuat instance dari PeminjamanController
        PeminjamanController peminjamanController = new PeminjamanController();

        // Mendapatkan daftar semua peminjaman
        List<Peminjaman> listPeminjaman = peminjamanController.getAllPeminjaman();

        // Nama kolom tabel
        String[] columnNames = {"ID", "Nama Barang", "Nama Peminjam", "NIM", "Tanggal Pinjam", "Tanggal Kembali"};
        DefaultTableModel tableModel = new DefaultTableModel(columnNames, 0);

        // Mengisi data ke model tabel
        for (Peminjaman peminjaman : listPeminjaman) {
            Object[] rowData = {
                peminjaman.getIdPeminjaman(),
                peminjaman.getNamaBarang(),
                peminjaman.getNamaAnggota(),
                peminjaman.getNim(),
                peminjaman.getTanggalPeminjaman(),
                peminjaman.getTanggalPengembalian()
            };
            tableModel.addRow(rowData);
        }

        // Menetapkan model ke tabel
        tbPeminjaman.setModel(tableModel);
    }

