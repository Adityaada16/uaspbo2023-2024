    private void jButtonTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonTambahActionPerformed
        try {
            // Ambil data dari form
            String tanggalPinjamStr = TfTglPeminjaman.getText();
            String tanggalKembaliStr = TfTglPengembalian.getText();

            // Ambil barang dan anggota yang dipilih dari ComboBox
            String namaBarang = (String) cbBarang.getSelectedItem();
            String namaAnggota = (String) cbAnggota.getSelectedItem();

            // Membuat instance dari PeminjamanController
            PeminjamanController peminjamanController = new PeminjamanController();

            // Validasi input menggunakan controller
            peminjamanController.validatePeminjamanData(tanggalPinjamStr, tanggalKembaliStr);

            // Konversi tanggal ke java.sql.Date
            java.sql.Date tanggalPinjam = java.sql.Date.valueOf(tanggalPinjamStr);
            java.sql.Date tanggalKembali = java.sql.Date.valueOf(tanggalKembaliStr);

            // Ambil ID dari HashMap
            int idBarang = getIdBarang(namaBarang);
            int idAnggota = getIdAnggota(namaAnggota);

            // Buat objek Peminjaman
            Peminjaman peminjaman = new Peminjaman(idAnggota, idBarang, tanggalPinjam, tanggalKembali);

            // Tambahkan peminjaman ke database
            peminjamanController.addPeminjaman(peminjaman);

            // Menampilkan dialog konfirmasi berhasil
            JOptionPane.showMessageDialog(this, "Peminjaman berhasil ditambahkan!", "Sukses", JOptionPane.INFORMATION_MESSAGE);

            // Refresh tabel setelah penambahan
            showDataTable();
            resetField();
        } catch (IllegalArgumentException e) {
            // Menampilkan dialog error untuk validasi
            JOptionPane.showMessageDialog(this, e.getMessage(), "Input Error", JOptionPane.ERROR_MESSAGE);
        } catch (Exception e) {
            // Menampilkan dialog error umum
            JOptionPane.showMessageDialog(this, "Gagal menambahkan peminjaman: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButtonTambahActionPerformed
    
