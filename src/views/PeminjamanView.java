    private void jButtonEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEditActionPerformed
        try {
            // Ambil ID peminjaman yang dipilih
            int idPeminjaman = id_peminjaman; // pastikan ini adalah ID yang benar dari peminjaman yang dipilih

            // Ambil data dari form
            String tanggalPinjamStr = TfTglPeminjaman.getText();
            String tanggalKembaliStr = TfTglPengembalian.getText();

            // Ambil barang dan anggota yang dipilih dari ComboBox
            String namaBarang = (String) cbBarang.getSelectedItem();
            String namaAnggota = (String) cbAnggota.getSelectedItem();

            // Membuat instance dari PeminjamanController
            PeminjamanController peminjamanController = new PeminjamanController();

            // Validasi input menggunakan controller
            peminjamanController.validatePeminjamanData(tanggalPinjamStr, tanggalKembaliStr);

            // Konversi string tanggal ke java.sql.Date
            java.sql.Date tanggalPinjam = java.sql.Date.valueOf(tanggalPinjamStr);
            java.sql.Date tanggalKembali = java.sql.Date.valueOf(tanggalKembaliStr);

            // Ambil ID dari HashMap atau metode lain yang Anda gunakan
            int idBarang = getIdBarang(namaBarang);
            int idAnggota = getIdAnggota(namaAnggota);

            // Pastikan ID peminjaman yang valid telah dipilih
            if (idPeminjaman != -1) {
                // Membuat objek Peminjaman dengan konstruktor yang sesuai
                Peminjaman peminjaman = new Peminjaman(idPeminjaman, idAnggota, idBarang, tanggalPinjam, tanggalKembali,
                    namaAnggota, "NIM_PLACEHOLDER", "PRODI_PLACEHOLDER", namaBarang);

                // Memperbarui data peminjaman di database
                peminjamanController.updatePeminjaman(peminjaman);
                JOptionPane.showMessageDialog(this, "Data peminjaman berhasil diperbarui!", "Sukses", JOptionPane.INFORMATION_MESSAGE);

                // Reset field input dan refresh tabel
                resetField();
                showDataTable();
            } else {
                JOptionPane.showMessageDialog(this, "Silahkan pilih data peminjaman yang valid!", "Gagal", JOptionPane.ERROR_MESSAGE);
            }
        } catch (IllegalArgumentException e) {
            // Menampilkan dialog error untuk validasi
            JOptionPane.showMessageDialog(this, e.getMessage(), "Input Error", JOptionPane.ERROR_MESSAGE);
        } catch (Exception e) {
            // Menampilkan dialog error umum
            JOptionPane.showMessageDialog(this, "Terjadi kesalahan: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButtonEditActionPerformed

    private void jButtonDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonDeleteActionPerformed
        int idPeminjaman = id_peminjaman; // Ambil ID peminjaman yang dipilih

        if (idPeminjaman != -1) {
            PeminjamanController peminjamanController = new PeminjamanController();

            // Menghapus data peminjaman dari database
            try {
                int confirm = JOptionPane.showConfirmDialog(this, "Apakah Anda yakin ingin menghapus peminjaman dengan ID: " 
                        + idPeminjaman + "?", "Konfirmasi Penghapusan", JOptionPane.YES_NO_OPTION);
                if (confirm == JOptionPane.YES_OPTION) {
                    peminjamanController.deletePeminjaman(idPeminjaman);
                    JOptionPane.showMessageDialog(this, "Data peminjaman berhasil dihapus!", "Sukses", JOptionPane.INFORMATION_MESSAGE);
                    resetField();
                    showDataTable();
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Terjadi kesalahan: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
                e.printStackTrace();
            }
        } else {
            JOptionPane.showMessageDialog(this, "Silahkan pilih data peminjaman terlebih dahulu", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButtonDeleteActionPerformed

    private void tbPeminjamanMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbPeminjamanMouseClicked
        // Mendapatkan indeks baris yang diklik
        int selectedRow = tbPeminjaman.getSelectedRow();

        // Pastikan ada baris yang dipilih
        if (selectedRow >= 0) {
            // Mendapatkan model tabel untuk mengakses data
            DefaultTableModel model = (DefaultTableModel) tbPeminjaman.getModel();

            // Mengambil data dari baris yang diklik
            String id = model.getValueAt(selectedRow, 0).toString();
            String namaBarang = model.getValueAt(selectedRow, 1).toString();
            String namaPeminjam = model.getValueAt(selectedRow, 2).toString();
            String nim = model.getValueAt(selectedRow, 3).toString();
            String tanggalPinjam = model.getValueAt(selectedRow, 4).toString();
            String tanggalKembali = model.getValueAt(selectedRow, 5).toString();

            // Menetapkan data ke field-field input
            id_peminjaman = Integer.parseInt(id); // Pastikan `id_peminjaman` adalah variabel yang menyimpan ID peminjaman
            cbBarang.setSelectedItem(namaBarang);
            cbAnggota.setSelectedItem(namaPeminjam);
            TfTglPeminjaman.setText(tanggalPinjam);
            TfTglPengembalian.setText(tanggalKembali);
        }
    }//GEN-LAST:event_tbPeminjamanMouseClicked

